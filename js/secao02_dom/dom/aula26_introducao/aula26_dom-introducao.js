// O DOM (Document Object Model) representa a estrutura do HTML como uma árvore de objetos.

// Cada elemento HTML é um nó que pode ser acessado e manipulado via JavaScript.

// document.getElementById(id) -> retorna o elemento com o ID especificado.
// document.querySelector(seletor) -> retorna o primeiro elemento que combina com o seletor CSS.
// document.querySelectorAll(seletor) -> retorna todos os elementos que combinam com o seletor CSS.

// Podemos alterar conteúdo, atributos, estilos e classes dos elementos via DOM.
// Ex: elemento.innerText, elemento.setAttribute(), elemento.style, elemento.classList

// Eventos (como cliques) podem ser usados para interagir com o DOM.
// Ex: elemento.addEventListener('click', funcao)

// O DOM só pode ser manipulado após o carregamento do conteúdo HTML.